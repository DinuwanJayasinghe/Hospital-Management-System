    private void jButton_EqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EqualActionPerformed

        String answer;
        secondnum = Double.parseDouble(txt_cal.getText());

        if (operations == "+") {
            result = firstnum + secondnum;
            answer = String.format("%.0f",result);
            txt_cal.setText(answer);
            txt_cal.setText(answer);
            
        }

        else if (operations == "-") {
            result = firstnum - secondnum;
            answer = String.format("%.0f",result);
            txt_cal.setText(answer);
        }

        else if (operations == "*") {
            result = firstnum * secondnum;
            answer = String.format("%.0f",result);
            txt_cal.setText(answer);
        }

        else if (operations == "/") {
            result = firstnum / secondnum;
            answer = String.format("%.0f",result);
            txt_cal.setText(answer);
        }

        else if (operations == "%") {
            result = firstnum % secondnum;
            answer = String.format("%.0f",result);
            txt_cal.setText(answer);
        }
    }//GEN-LAST:event_jButton_EqualActionPerformed

