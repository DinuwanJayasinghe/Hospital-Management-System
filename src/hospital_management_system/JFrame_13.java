    private void txt_calKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_calKeyTyped

          char c = evt.getKeyChar();
        
        if (!(Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || c == KeyEvent.VK_DELETE)){
                
            evt.consume();
            getToolkit().beep();
        }
        
    }//GEN-LAST:event_txt_calKeyTyped

