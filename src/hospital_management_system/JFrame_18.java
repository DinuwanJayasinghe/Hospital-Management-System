    private void jButton_EquealActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EquealActionPerformed
        String answer;
        secondnum = Double.parseDouble(jTXT_Display.getText());

        if (operation == "+") {

            result = firstnum + secondnum;
            answer = String.format("%.2f", result);
            jTXT_Display.setText(answer);
        } else if (operation == "-") {

            result = firstnum - secondnum;
            answer = String.format("%.2f", result);
            jTXT_Display.setText(answer);

        } else if (operation == "*") {

            result = firstnum * secondnum;
            answer = String.format("%.2f", result);
            jTXT_Display.setText(answer);

        } else if (operation == "/") {

            result = firstnum / secondnum;
            answer = String.format("%.2f", result);
            jTXT_Display.setText(answer);

        } else if (operation == "%") {

            result = firstnum % secondnum;
            answer = String.format("%.2f", result);
            jTXT_Display.setText(answer);

        }
    }//GEN-LAST:event_jButton_EquealActionPerformed

