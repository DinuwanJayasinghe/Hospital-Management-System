    private void jButton_ConverterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ConverterActionPerformed

        if ((jTextEnter3.getText().equals("")) || (jTextEnter4.getText().equals(""))) {//field empty tiyanna ba kiana cord eka

            JOptionPane.showMessageDialog(null, "You must enter value to compute", "Hospital Management System", JOptionPane.WARNING_MESSAGE);
        } else if (jCDistance.getSelectedItem().equals("Distance")) {

            double dst1 = Double.parseDouble(jTextEnter3.getText());
            double dst2 = Double.parseDouble(jTextEnter4.getText());
            double dst3;

            //distance = speed * time
            jLbl_Display_4.setText("Distance = ");
            jLbl_Display_2.setText("Speed  X");
            jLbl_Display_5.setText("Time");

            dst3 = dst1 * dst2;
            String value = String.format("%.2f", dst3);
            jLbl_Result.setText(value);

        } else if (jCDistance.getSelectedItem().equals("Speed")) {

            double dst1 = Double.parseDouble(jTextEnter3.getText());
            double dst2 = Double.parseDouble(jTextEnter4.getText());
            double dst3;

            //Speed = Distance / Time
            jLbl_Display_4.setText("Speed = ");
            jLbl_Display_2.setText("Distance  ÷ ");
            jLbl_Display_5.setText("Time");

            dst3 = dst1 / dst2;
            String value = String.format("%.2f", dst3);
            jLbl_Result.setText(value);

        } else if (jCDistance.getSelectedItem().equals("Time")) {

            double dst1 = Double.parseDouble(jTextEnter3.getText());
            double dst2 = Double.parseDouble(jTextEnter4.getText());
            double dst3;

            //Time = Distance / Speed
            jLbl_Display_4.setText("Time = ");
            jLbl_Display_2.setText("Distance  ÷ ");
            jLbl_Display_5.setText("Speed");

            dst3 = dst1 / dst2;
            String value = String.format("%.2f", dst3);
            jLbl_Result.setText(value);

        }
    }//GEN-LAST:event_jButton_ConverterActionPerformed

